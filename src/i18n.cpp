void I18n::setLang(const QString &l) {
    if (m_lang == l) return;
    m_lang = l;
    QSettings s;
    s.setValue(QStringLiteral("language"), m_lang);
    emit langChanged();
}

QString I18n::t(const QString &es) const {
    if (m_lang == QStringLiteral("es")) return es;
    return m_es_en.value(es, es);
}
